package com.epam.task11.entity.db;

import com.epam.task11.annotations.DBField;
import com.epam.task11.util.Constants;

import java.io.Serializable;

public class Manufacturer implements Serializable {
    @DBField(DBFieldName = Constants.DB_MANUFACTURER_ID)
    private int id;
    @DBField(DBFieldName = Constants.DB_MANUFACTURER_NAME)
    private String name;

    public Manufacturer() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Manufacturer that = (Manufacturer) o;

        if (id != that.id) return false;
        return name != null ? name.equals(that.name) : that.name == null;

    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "Manufacturer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
